# 4. Dicionários como Fonte de Dados
# Contexto: Em APIs modernas, os dados geralmente chegam como dicionários (JSON).

# Tarefa: Crie um dicionário representando um produto: meu_produto = {'nome': 'Mouse Gamer', 'preco': 120.50}.

